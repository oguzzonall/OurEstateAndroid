package com.oguzzonall.e_emlakapp.Models;

public class RolsPojo {
    private String Rols;

    public String getRols() {
        return Rols;
    }

    public void setRols(String rols) {
        Rols = rols;
    }

    @Override
    public String toString() {
        return "RolsPojo{" +
                "Rols='" + Rols + '\'' +
                '}';
    }
}
